/**
 * ‡∏´‡∏≤ Quick Response ‡∏à‡∏≤‡∏Å keywords
 */
export function findQuickResponse(userText: string): QuickResponseTemplate | null {
  const lower = userText.toLowerCase();

  for (const template of QUICK_RESPONSES) {
    for (const keyword of template.keywords) {
      if (lower.includes(keyword.toLowerCase())) {
        return template;
      }
    }
  }

  return null;
}

